    folded = _fold_arabic(candidate_reply)
    pressure_tokens = [
        r"حول\s+دلوقتي",
        r"حول\s+الان",
        r"ادفع\s+حالا",
        r"ادفع\s+دلوقتي",
        r"ارسل\s+الفلوس",
        r"تحويل\s+البنك\s+فورا",
        r"ارسل\s+رقم\s+الدفع\s+الان",
        r"ابعت\s+الدفع",
        r"ابعتلي\s+الدفع",
        r"يلا\s+ابعت\s+الدفع",
        r"ابعت(لي)?\s+(ال)?دفع",
    ]
    return any(re.search(_fold_arabic(pat), folded, re.I) for pat in pressure_tokens)


