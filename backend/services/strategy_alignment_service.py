"""
strategy_alignment_service.py — Conversational Strategy Alignment Enforcement
==============================================================================
Enforces strategy alignment on candidate replies generated by the AI sales runtime.
Prevents high-risk policy violations (e.g., selling after rejection, payment pressure
during browsing/stalled, resetting purchase commitment to discovery, untrusted commercial claims).
"""

import json
import logging
